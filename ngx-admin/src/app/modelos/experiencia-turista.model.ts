// Definición de la clase ExperienciaTurista
export class ExperienciaTurista {
    _id?: string; // Identificador único de la experiencia del turista 
    fechaParticipacion?: string; // Fecha en que el turista participó en el recorrido
    calificacion?: string; // Calificación dada por el turista a la experiencia
    comentario?: string; // Comentario del turista sobre la experiencia
    id_Turista?: string; // Identificador del turista asociado a la experiencia
    id_Recorrido?: string; // Identificador del recorrido asociado a la experiencia
}
