// Definición de la clase Recorrido
export class Recorrido {
    _id?: string; // Identificador único del recorrido 
    nombreRecorrido?: string; // Nombre del recorrido
    duracionEstimada?: string; // Duración estimada del recorrido
    precio?: string; // Precio del recorrido
    ubicacionInicio?: string; // Ubicación de inicio del recorrido
    ubicacionFinal?: string; // Ubicación final del recorrido
    estaciones?: string; // Información sobre las estaciones del recorrido
    podcast?: string; // Enlace al podcast asociado al recorrido
}
