// Definición de la clase Usuario
export class Usuario {
    _id?: string; // Identificador único del usuario 
    seudonimo?: string; // Seudónimo o nombre de usuario del usuario
    correo?: string; // Dirección de correo electrónico del usuario
    contrasena?: string; // Contraseña del usuario
    token?: string; // Token de autenticación asociado al usuario
}
